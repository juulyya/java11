public class CinemaClass {

    private String cinemaName;

    private String cinemaGenre;

    public CinemaClass(String cinemaName, String cinemaGenre) {
        this.cinemaName = cinemaName;
        this.cinemaGenre = cinemaGenre;
    }

    public String getCinemaName() {
        return cinemaName;
    }

    public String getCinemaGenre() {
        return cinemaGenre;
    }
}
